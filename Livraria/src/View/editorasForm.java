    private void bExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bExcluirActionPerformed
        editoras e = new editoras();
        editorasDao dao = new editorasDao();

        e.setId(Integer.parseInt(tfID.getText()));

        dao.excluir(e);
        carregarDados();
    }//GEN-LAST:event_bExcluirActionPerformed

    private void bAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bAlterarActionPerformed
        editoras e = new editoras();
        editorasDao dao = new editorasDao();

        e.setNome(tfNome.getText());
        e.setCidade(tfCidade.getText());
        e.setEstado(tfEstado.getText());
        e.setId(Integer.parseInt(tfID.getText()));

        dao.alterar(e);
        carregarDados();
    }//GEN-LAST:event_bAlterarActionPerformed

    private void tEditorasMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tEditorasMouseClicked

        tfID.setText(tEditoras.getValueAt(tEditoras.getSelectedRow(), 0).toString());
        tfNome.setText(tEditoras.getValueAt(tEditoras.getSelectedRow(), 1).toString());
        tfCidade.setText(tEditoras.getValueAt(tEditoras.getSelectedRow(), 2).toString());
        tfEstado.setText(tEditoras.getValueAt(tEditoras.getSelectedRow(), 3).toString());

    }//GEN-LAST:event_tEditorasMouseClicked

    private void bSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bSalvarActionPerformed
        editoras e = new editoras();
        editorasDao dao = new editorasDao();

        e.setNome(tfNome.getText());
        e.setCidade(tfCidade.getText());
        e.setEstado(tfEstado.getText());

        dao.salvar(e);
        carregarDados();

    }//GEN-LAST:event_bSalvarActionPerformed

