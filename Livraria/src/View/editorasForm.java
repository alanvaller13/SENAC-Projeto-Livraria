    public void carregarDados() {
        editorasDao dao = new editorasDao();
        DefaultTableModel tabela = (DefaultTableModel) tEditoras.getModel();
        tabela.setNumRows(0);

        for (editoras e : dao.listar()) {
            tabela.addRow(new Object[]{
                e.getId(),
                e.getNome(),
                e.getCidade(),
                e.getEstado(),});
        }
    }

    public void pesquisarDados() {
        editorasDao dao = new editorasDao();
        DefaultTableModel tabela = (DefaultTableModel) tEditoras.getModel();
        tabela.setNumRows(0);

        for (editoras e : dao.pesquisarDados(tfPesquisar.getText())) {
            tabela.addRow(new Object[]{
                e.getId(),
                e.getNome(),
                e.getCidade(),
                e.getEstado(),});
        }
    }
    
